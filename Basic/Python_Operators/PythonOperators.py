# Bitwise Operators
x = 5 #0b0101
y = 3 #0b0011
print ("Bitwise Operators")
print ("x & y:", x & y)   # 0001
print ("x | y:", x | y)   # 0111
print ("x ^ y:", x ^ y)   # 0110
print ("~x:", ~x)         # bitwise NOT
print ("x << 1:", x << 1) # Left shift
print ("x >> 1:", x >> 1) # Right Shift



