# Logical Operators
print ("Logical Operators:")
print ("a > 5 and b < 5:", a > 5 and b < 5)
print ("a > 5 and b > 5:", a > 5 or b > 5)
print ("not(a > 5):", not(a > 5))
print ()

# Identity Operators
print ("Identity Operators:")
